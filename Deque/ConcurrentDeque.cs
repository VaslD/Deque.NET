        /// <summary>
        ///     Takes a moment-in-time snapshot of the deque.
        /// </summary>
        /// <returns>A list representing a moment-in-time snapshot of the deque.</returns>
        /// <remarks>
        ///     The algorithm runs in linear O(n) time.
        ///     This implementation relies on the following invariant:
        ///     If at time t, x was the leftmost node and y was the rightmost node,
        ///     regardless of how many nodes are pushed/popped from either end thereafter, the paths
        ///     (a) x->a (obtained by traversing the deque recursively using a node's right pointer starting from x), and
        ///     (b) y->b (obtained by traversing the deque recursively using a node's left pointer starting from y)
        ///     will always have at least 1 node in common.
        ///     This means that, for a given x and y, even if the deque is mutated during the algorithm's
        ///     execution, we can always rebuild the original x-y sequence by finding a node c, common to both
        ///     x->a and y->b paths, and merging the paths by the common node.
        /// </remarks>
        private List<T> ToList()
        {
            //try to grab a reference to a stable anchor (fast route)
            var anchor = _anchor;
            anchor.Validate();

            //try to grab a reference to a stable anchor (slow route)
            if (anchor._status != DequeStatus.Stable)
            {
                var spinner = new SpinWait();
                do
                {
                    anchor = _anchor;
                    anchor.Validate();

                    spinner.SpinOnce();
                } while (anchor._status != DequeStatus.Stable);
            }

            var x = anchor._left;
            var y = anchor._right;

            //check if deque is empty
            if (x == null) return new List<T>();

            //check if deque has only 1 item
            if (x == y) return new List<T> {x._value};

            var xaPath  = new List<Node>();
            var current = x;
            while (current != null && current != y)
            {
                xaPath.Add(current);
                current = current._right;
            }

            /*
             * If the 'y' node hasn't been popped from the right side of the deque,
             * then we should still be able to find the original x-y sequence 
             * using a node's right pointer.
             * 
             * If 'current' does not equal 'y', then the 'y' node must have been popped by
             * another thread during the while loop and the traversal wasn't successful.
             */
            if (current == y)
            {
                xaPath.Add(current);
                return xaPath.Select(node => node._value).ToList();
            }

            /*
             * If the 'y' node has been popped from the right end, we need to find all nodes that have
             * been popped from the right end and rebuild the original sequence.
             * 
             * To do this, we need to traverse the deque from right to left (using a node's left pointer)
             * until we find a node c common to both x->a and y->b paths. Such a node is either:
             * (a) currently part of the deque or
             * (b) the last node of the x->a path (i.e., node 'a') or
             * (c) the last node to be popped from the left (if all nodes between 'x' and 'y' were popped from the deque).
             * 
             * -- Predicate (a) --
             * A node belongs to the deque if node.left.right == node (except for the leftmost node) if the deque has > 1 nodes.
             * If the deque has exactly one node, we know we've found that node if:
             * (1) all nodes to its right in the x-y sequence don't fall under predicates (a), (b) and (c) and
             * (2) node.left == null
             * 
             * -- Predicate (b) --
             * True for a node n if n == a
             * 
             * -- Predicate (c) --
             * True for a node n if:
             * (1) all nodes to its right in the x-y sequence don't fall under predicates (a), (b) and (c) and
             * (2) node.left == null
             */
            current = y;
            var a      = xaPath.Last();
            var ycPath = new Stack<Node>();
            while (current._left != null && current._left._right != current && current != a)
            {
                ycPath.Push(current);
                current = current._left;
            }

            //this node is common to the list and the stack
            var common = current;
            ycPath.Push(common);

            /*
             * Merge the x->a and the y->c paths by the common node.
             * This is done by removing the nodes in x->a that come after c,
             * and appending all nodes in the y->c path in reverse order.
             * Since we used a LIFO stack to store all nodes in the y->c path,
             * we can simply iterate over it to reverse the order in which they were inserted.
             */
            var xySequence = xaPath
                            .TakeWhile(node => node != common)
                            .Select(node => node._value)
                            .Concat(ycPath.Select(node => node._value));

            return xySequence.ToList();
        }

        internal class Anchor
        {
            internal readonly Node        _left;
            internal readonly Node        _right;
            internal readonly DequeStatus _status;

            internal Anchor()
            {
                _right  = _left = null;
                _status = DequeStatus.Stable;
            }

            internal Anchor(Node left, Node right, DequeStatus status)
            {
                _left   = left;
                _right  = right;
                _status = status;
            }

            /// <summary>
            ///     Validates the anchor's state.
            /// </summary>
            [Conditional("DEBUG")]
            public void Validate()
            {
                //assert that either both pointers are null or not null
                Debug.Assert(_left == null && _right == null || _left != null && _right != null);

                //assert that if the anchor is empty, then it is stable
                if (_left == null) Debug.Assert(_status == DequeStatus.Stable);
            }
        }

        internal enum DequeStatus
        {
            Stable,
            LPush,
            RPush
        }

        internal class Node
        {
            internal readonly T    _value;
            internal volatile Node _left;
            internal volatile Node _right;

            internal Node(T value) { _value = value; }
        }

        #region Constructors

        /// <summary>
        ///     Initializes a new instance of the <see cref="ConcurrentDeque{T}" /> class.
        /// </summary>
        public ConcurrentDeque() { _anchor = new Anchor(); }

        /// <summary>
        ///     Initializes a new instance of the <see cref="ConcurrentDeque{T}" /> class that contains elements copied from the
        ///     specified collection.
        /// </summary>
        /// <param name="collection">
        ///     The collection whose elements are copied to the new <see cref="ConcurrentDeque{T}" />.
        /// </param>
        /// <exception cref="ArgumentNullException">
        ///     The <paramref name="collection" /> argument is <c>null</c>.
        /// </exception>
        public ConcurrentDeque(IEnumerable<T> collection)
        {
            if (collection == null) throw new ArgumentNullException(nameof(collection));

            InitializeFromCollection(collection);
        }

        #endregion

        #region Properties

        /// <summary>
        ///     Gets the number of elements contained in the <see cref="ConcurrentDeque{T}" />.
        /// </summary>
        /// <returns>
        ///     The number of elements contained in the <see cref="ConcurrentDeque{T}" />.
        /// </returns>
        public Int32 Count => ToList().Count;

        /// <summary>
        ///     Gets a value that indicates whether the <see cref="ConcurrentDeque{T}" /> is empty.
        /// </summary>
        /// <returns>
        ///     <c>true</c> if the <see cref="ConcurrentDeque{T}" /> is empty; otherwise, <c>false</c>.
        /// </returns>
        public Boolean IsEmpty => _anchor._left == null;

        #endregion

        #region Explicit Interface Implementations

        /// <summary>
        ///     Gets an object that can be used to synchronize access to the <see cref="ConcurrentDeque{T}" />. This property is
        ///     not supported.
        /// </summary>
        /// <returns>
        ///     Returns <c>null</c>.
        /// </returns>
        /// <exception cref="NotSupportedException">
        ///     The SyncRoot property is not supported.
        /// </exception>
        Object ICollection.SyncRoot
            => throw new
                   NotSupportedException("This property may not be used for the synchronization of concurrent collections.");

        /// <summary>
        ///     Gets a value indicating whether access to the <see cref="ConcurrentDeque{T}" /> is synchronized with the SyncRoot.
        /// </summary>
        /// <returns>
        ///     <c>true</c> if access to the <see cref="ConcurrentDeque{T}" /> is synchronized with the SyncRoot; otherwise,
        ///     <c>false</c>. For <see cref="ConcurrentDeque{T}" />, this property always returns false.
        /// </returns>
        Boolean ICollection.IsSynchronized => false;

        /// <summary>
        ///     Copies the collection elements to an existing one-dimensional <see cref="Array" />, starting at the specified array
        ///     index.
        /// </summary>
        /// <param name="array">
        ///     The one-dimensional <see cref="Array" /> that is the destination of the elements copied from the collection. The
        ///     Array must have zero-based indexing.
        /// </param>
        /// <param name="index">
        ///     The zero-based index in <paramref name="array" /> at which copying begins.
        /// </param>
        /// <exception cref="ArgumentNullException">
        ///     <paramref name="array" /> is <c>null</c>.
        /// </exception>
        /// <exception cref="ArgumentOutOfRangeException">
        ///     <paramref name="index" /> is less than 0.
        /// </exception>
        /// <exception cref="ArgumentException">
        ///     <paramref name="array" /> is multidimensional; or,
        ///     <paramref name="array" /> does not have zero-based indexing; or,
        ///     The number of elements in the source <see cref="ConcurrentDeque{T}" /> is greater than the available space from
        ///     <paramref name="index" /> to the end of the destination <paramref name="array" />; or,
        ///     The type of the source <see cref="ConcurrentDeque{T}" /> cannot be cast automatically to the type of the
        ///     destination array.
        /// </exception>
        void ICollection.CopyTo(Array array, Int32 index)
        {
            if (array == null) throw new ArgumentNullException(nameof(array));

            // We must be careful not to corrupt the array, so we will first accumulate an internal list of elements that we will then copy to the array. This requires some extra allocation, but is necessary since we don't know up front whether the array is sufficiently large to hold the contents.
            ((ICollection) ToList()).CopyTo(array, index);
        }

        /// <summary>
        ///     Returns an enumerator that iterates through a collection.
        /// </summary>
        /// <returns>
        ///     An <see cref="IEnumerator" /> object that can be used to iterate through the collection.
        /// </returns>
        /// <remarks>
        ///     The enumeration represents a moment-in-time snapshot of the deque. It does not reflect any updates to the
        ///     collection after <see cref="GetEnumerator" /> was called. The enumerator is safe to use concurrently with reads and
        ///     writes on the deque.
        /// </remarks>
        IEnumerator IEnumerable.GetEnumerator() { return GetEnumerator(); }

        #endregion

        #region Serialization

        [OnSerializing]
        private void OnSerializing(StreamingContext context) { _serializationArray = ToArray(); }

        [OnDeserialized]
        private void OnDeserialized(StreamingContext context)
        {
            Contract.Assert(_serializationArray != null);

            InitializeFromCollection(_serializationArray);
            _serializationArray = null;
        }

        #endregion

        #region Methods

        /// <summary>
        ///     Removes all objects from the <see cref="ConcurrentDeque{T}" />.
        /// </summary>
        public void Clear()
        {
            //Clear the list by setting the anchor to a new one.
            _anchor = new Anchor();
        }

        /// <summary>
        ///     Copies the <see cref="ConcurrentDeque{T}" /> elements to an existing one-dimensional <see cref="Array" />, starting at the
        ///     specified array index.
        /// </summary>
        /// <param name="array">
        ///     The one-dimensional <see cref="Array" /> that is the destination of the elements copied from
        ///     <see cref="ConcurrentDeque{T}" />. The Array must have zero-based indexing.
        /// </param>
        /// <param name="index">
        ///     The zero-based index in <paramref name="array" /> at which copying begins.
        /// </param>
        /// <exception cref="ArgumentNullException">
        ///     <paramref name="array" /> is <c>null</c>.
        /// </exception>
        /// <exception cref="ArgumentOutOfRangeException">
        ///     <paramref name="index" /> is less than 0.
        /// </exception>
        /// <exception cref="ArgumentException">
        ///     The number of elements in the source <see cref="ConcurrentDeque{T}" /> is greater than the available space from
        ///     <paramref name="index" /> to the end of the destination <paramref name="array" />.
        /// </exception>
        public void CopyTo(T[] array, Int32 index)
        {
            if (array == null) throw new ArgumentNullException(nameof(array));

            // We must be careful not to corrupt the array, so we will first accumulate an
            // internal list of elements that we will then copy to the array. This requires
            // some extra allocation, but is necessary since we don't know up front whether 
            // the array is sufficiently large to hold the deque's contents.
            ToList().CopyTo(array, index);
        }

        /// <summary>
        ///     Returns an enumerator that iterates through the <see cref="ConcurrentDeque{T}"/>.
        /// </summary>
        /// <returns>
        /// An enumerator for the <see cref="ConcurrentDeque{T}" />.
        /// </returns>
        /// <remarks>
        ///     The enumeration represents a moment-in-time snapshot of the contents
        ///     of the deque. It does not reflect any updates to the collection after
        ///     <see cref="GetEnumerator" /> was called. The enumerator is safe to use concurrently with reads from and writes to the deque.
        /// </remarks>
        public IEnumerator<T> GetEnumerator() => ToList().GetEnumerator();

        /// <summary>
        ///     Copies the items stored in the <see cref="ConcurrentDeque{T}"/> to a new array.
        /// </summary>
        /// <returns>
        ///     A new array containing a snapshot of elements copied from the <see cref="ConcurrentDeque{T}" />.
        /// </returns>
        public T[] ToArray() => ToList().ToArray();

        #endregion
    }
