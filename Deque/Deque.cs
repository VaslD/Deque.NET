using System.Diagnostics;
using System.Extensions;
using System.Linq;
using System.Runtime.CompilerServices;
using System.Threading;

// Todo: For unit-test only. Comment the next line in production.
[assembly: InternalsVisibleTo("Deque.NUnit")]

// ReSharper disable once CheckNamespace
namespace System.Collections.Generic
{
    /// <summary>
    ///     Represents a strongly typed deque (double-ended queue) of objects. Provides methods to insert and delete elements
    ///     from both ends.
    /// </summary>
    /// <typeparam name="T">
    ///     The type of elements in the deque.
    /// </typeparam>
    [DebuggerDisplay("Count = {Count}")]
    [DebuggerTypeProxy(typeof(DequeDebugView<>))]
    [Serializable]
    public class Deque<T> : IDeque<T>
    {
        private const Int32 DefaultCapacity = 4;

        private static readonly T[] EmptyBuffer = new T[0];

        /// <summary>
        ///     Internal ring buffer that holds all elements.
        /// </summary>
        private T[] _buffer;

        /// <summary>
        ///     The offset used to calculate the position of the left-most item in the buffer.
        /// </summary>
        private Int32 _leftIndex;

        [NonSerialized]
        private Object _syncRoot;

        private Int32 _version;

        /// <summary>
        ///     Gets or sets the total number of elements the internal data structure can hold without resizing.
        /// </summary>
        /// <exception cref="ArgumentOutOfRangeException">
        ///     <see cref="Capacity" /> is set to a value less than <see cref="Count" />.
        /// </exception>
        internal Int32 Capacity
        {
            get => _buffer.Length;
            set
            {
                if (value < Count)
                    throw new ArgumentOutOfRangeException(nameof(value), "capacity was less than the current size.");

                if (value == Capacity) return;

                var newBuffer = new T[value];

                CopyTo(newBuffer, 0);

                LeftIndex = 0;
                _buffer   = newBuffer;
            }
        }

        /// <summary>
        ///     Determines whether the <see cref="Deque{T}" /> "loops around" the array's boundary, i.e. whether the right-most
        ///     index is lower than the left-most index.
        /// </summary>
        private Boolean IndexExceedsBoundary => Count > Capacity - LeftIndex;

        private Int32 LeftIndex { get => _leftIndex; set => _leftIndex = CalculateIndex(value); }

        private T Left { get => this[0]; set => this[0] = value; }

        private T Right { get => this[Count - 1]; set => this[Count - 1] = value; }

        private void InitializeFromCollection(IEnumerable<T> enumerable)
        {
            var collection = enumerable as ICollection<T> ?? enumerable.ToArray();

            var count = collection.Count;

            if (count == 0)
            {
                _buffer = EmptyBuffer;
            }
            else
            {
                _buffer = new T[count];
                collection.CopyTo(_buffer, 0);
                Count = count;
            }
        }

        /// <summary>
        ///     Ensures that the capacity of this list is at least the given minimum value. If the current capacity of the list is
        ///     less, the capacity is increased to twice the current capacity
        ///     or the minimum required, whichever is larger.
        /// </summary>
        /// <param name="min">The minimum capacity required.</param>
        private void EnsureCapacity(Int32 min)
        {
            if (Capacity < min)
            {
                var newCapacity = Capacity == 0 ? DefaultCapacity : Capacity * 2;
                newCapacity = Math.Max(newCapacity, min);
                Capacity    = newCapacity;
            }
        }

        /// <summary>
        ///     Uses modular arithmetic to calculate the correct ring buffer index for a given (possibly out-of-bounds) index.
        ///     If <paramref name="position" /> is over the array's upper boundary, the returned index "wraps-around" the end.
        /// </summary>
        /// <param name="position">The possibly out-of-bounds index.</param>
        /// <returns>The ring buffer index.</returns>
        private Int32 CalculateIndex(Int32 position)
        {
            // Find the position in the range [0, Capacity-1] using modular arithmetic.
            if (Capacity != 0) return position.Mod(Capacity);

            // If the capacity is 0, the left-most index must also be 0.
            Debug.Assert(_leftIndex == 0);

            return 0;
        }

        /// <summary>
        ///     Calculates the ring buffer index corresponding to a given "virtual index". A virtual index is the index of an item
        ///     as seen from an enumerator's perspective, i.e. index as if items were laid
        ///     out sequentially starting at index 0. Therefore, a virtual index is in the range [0, Count - 1].
        /// </summary>
        /// <param name="index">The virtual index of an element.</param>
        /// <returns>A ring buffer index for internal use.</returns>
        private Int32 VirtualIndexToBufferIndex(Int32 index)
        {
            if (index < 0 || index >= Count)
                throw new ArgumentOutOfRangeException(nameof(index),
                                                      "Index was out of range. Must be non-negative and less than the size of the collection.");

            // Apply LeftIndex offset and modular arithmetic.
            return CalculateIndex(LeftIndex + index);
        }

        /// <summary>
        ///     Enumerates the elements of a <see cref="Deque{T}" />.
        /// </summary>
        [Serializable]
        public struct Enumerator : IEnumerator<T>
        {
            private readonly Deque<T> _deque;
            private readonly Int32    _version;
            private          T        _current;
            private          Int32    _index;

            internal Enumerator(Deque<T> deque)
            {
                _deque   = deque;
                _version = _deque._version;
                _current = default(T);
                _index   = -1;
            }

            /// <summary>
            ///     Advances the enumerator to the next element of the <see cref="Deque{T}" />.
            /// </summary>
            /// <returns>
            ///     <c>true</c> if the enumerator was successfully advanced to the next element; <c>false</c> if the enumerator has
            ///     passed the end of the collection.
            /// </returns>
            /// <exception cref="InvalidOperationException">
            ///     The collection was modified after the enumerator was created.
            /// </exception>
            public Boolean MoveNext()
            {
                Validate();

                if (_index == -2) return false;

                _index++;
                if (_index == _deque.Count)
                {
                    _index   = -2;
                    _current = default(T);
                    return false;
                }

                _current = _deque[_index];
                return true;
            }

            /// <summary>
            ///     Sets the enumerator to its initial position, which is before the first element in the collection.
            /// </summary>
            /// <exception cref="InvalidOperationException">
            ///     The collection was modified after the enumerator was created.
            /// </exception>
            void IEnumerator.Reset()
            {
                Validate();

                _index   = -1;
                _current = default(T);
            }

            /// <summary>
            ///     Gets the element in the <see cref="Deque{T}" /> at the current position of the enumerator.
            /// </summary>
            public T Current
            {
                get
                {
                    if (_index == -1) throw new InvalidOperationException("Enumeration has not started.");
                    if (_index < 0) throw new InvalidOperationException("Enumeration was ended.");

                    return _current;
                }
            }

            /// <summary>
            ///     Gets the element at the current position of the enumerator.
            /// </summary>
            Object IEnumerator.Current => Current;

            /// <summary>
            ///     Releases all resources used by the <see cref="Enumerator" />.
            /// </summary>
            public void Dispose()
            {
                _index   = -2;
                _current = default(T);
            }

            /// <summary>
            ///     Validate the <see cref="Enumerator" />'s compatibility.
            /// </summary>
            /// <exception cref="InvalidOperationException">
            ///     The collection was modified after the enumerator was created.
            /// </exception>
            private void Validate()
            {
                if (_version != _deque._version)
                    throw new
                        InvalidOperationException("Collection was modified; enumeration operation may not execute.");
            }
        }

        #region Constructors

        /// <summary>
        ///     Initializes a new instance of the <see cref="Deque{T}" /> class that is empty and has the default initial capacity.
        /// </summary>
        public Deque() { _buffer = EmptyBuffer; }

        /// <summary>
        ///     Initializes a new instance of the <see cref="Deque{T}" /> class that is empty and has the specified initial
        ///     capacity.
        /// </summary>
        /// <param name="capacity">
        ///     The number of elements that the new <see cref="Deque{T}" /> can initially store.
        /// </param>
        /// <exception cref="ArgumentOutOfRangeException">
        ///     <paramref name="capacity" /> is less than 0.
        /// </exception>
        public Deque(Int32 capacity)
        {
            if (capacity < 0)
                throw new ArgumentOutOfRangeException(nameof(capacity), "Capacity cannot be less than zero.");

            _buffer = capacity == 0 ? EmptyBuffer : new T[capacity];
        }

        /// <summary>
        ///     Initializes a new instance of the <see cref="Deque{T}" /> class that contains elements copied from the specified
        ///     collection and has sufficient capacity to accommodate the number of elements copied.
        /// </summary>
        /// <param name="collection">
        ///     The collection whose elements are copied to the new <see cref="Deque{T}" />.
        /// </param>
        /// <exception cref="ArgumentNullException">
        ///     <paramref name="collection" /> is <c>null</c>.
        /// </exception>
        public Deque(IEnumerable<T> collection)
        {
            if (collection == null) throw new ArgumentNullException(nameof(collection));

            InitializeFromCollection(collection);
        }

        #endregion

        #region Properties

        /// <summary>
        ///     Gets the element at the specified index.
        /// </summary>
        /// <param name="index">
        ///     The zero-based index of the element to get.
        /// </param>
        /// <returns>
        ///     The element at the specified index.
        /// </returns>
        /// <exception cref="ArgumentOutOfRangeException">
        ///     <paramref name="index" /> is not a valid index in the <see cref="Deque{T}" />.
        /// </exception>
        internal T this[Int32 index]
        {
            get => _buffer[VirtualIndexToBufferIndex(index)];
            private set => _buffer[VirtualIndexToBufferIndex(index)] = value;
        }

        /// <summary>
        ///     Gets the number of elements contained in the <see cref="Deque{T}" />.
        /// </summary>
        public Int32 Count { get; private set; }

        /// <summary>
        ///     Gets a value indicating whether access to the <see cref="Deque{T}" /> is synchronized (thread safe).
        /// </summary>
        /// <returns>
        ///     <c>true</c> if access to the <see cref="Deque{T}" /> is synchronized (thread safe); otherwise, false. In the
        ///     default implementation of <see cref="Deque{T}" />, this property always returns false.
        /// </returns>
        Boolean ICollection.IsSynchronized => false;

        /// <summary>
        ///     Gets an object that can be used to synchronize access to the <see cref="Deque{T}" />.
        /// </summary>
        /// <returns>
        ///     An object that can be used to synchronize access to the <see cref="Deque{T}" />. In the default implementation of
        ///     <see cref="Deque{T}" />, this property always returns the current instance.
        /// </returns>
        Object ICollection.SyncRoot
        {
            get
            {
                if (_syncRoot == null) Interlocked.CompareExchange(ref _syncRoot, new Object(), null);
                return _syncRoot;
            }
        }

        #endregion

        #region Methods

        /// <summary>
        ///     Removes all objects from the <see cref="Deque{T}" />.
        /// </summary>
        public void Clear()
        {
            // Clear the ring buffer to allow the GC to reclaim the references
            if (IndexExceedsBoundary)
            {
                Array.Clear(_buffer, LeftIndex, Capacity  - LeftIndex);
                Array.Clear(_buffer, 0,         LeftIndex + (Count - Capacity));
            }
            else
            {
                Array.Clear(_buffer, LeftIndex, Count);
            }

            Count     = 0;
            LeftIndex = 0;

            _version++;
        }

        /// <summary>
        ///     Determines whether an element is in the <see cref="Deque{T}" />.
        /// </summary>
        /// <param name="item">
        ///     The object to locate in the <see cref="Deque{T}" />. The value can be null for reference types.
        /// </param>
        /// <returns>
        ///     <c>true</c> if <paramref name="item" /> is found in the <see cref="Deque{T}" />; otherwise, false.
        /// </returns>
        public Boolean Contains(T item) { return this.Contains(item, EqualityComparer<T>.Default); }

        /// <summary>
        ///     Copies the <see cref="Deque{T}" /> elements to an existing one-dimensional <see cref="Array" />, starting at the
        ///     specified array index.
        /// </summary>
        /// <param name="array">
        ///     The one-dimensional <see cref="Array" /> that is the destination of the elements copied from
        ///     <see cref="Deque{T}" />. The Array must have zero-based indexing.
        /// </param>
        /// <param name="arrayIndex">
        ///     The zero-based index in <paramref name="array" /> at which copying begins.
        /// </param>
        /// <exception cref="ArgumentNullException">
        ///     <paramref name="array" /> is <c>null</c>.
        /// </exception>
        /// <exception cref="ArgumentOutOfRangeException">
        ///     <paramref name="arrayIndex" /> is less than 0.
        /// </exception>
        /// <exception cref="ArgumentException">
        ///     The number of elements in the source <see cref="Deque{T}" /> is greater than the available space from
        ///     <paramref name="arrayIndex" /> to the end of the destination <paramref name="array" />.
        /// </exception>
        public void CopyTo(T[] array, Int32 arrayIndex) { ((ICollection) this).CopyTo(array, arrayIndex); }

        /// <summary>
        ///     Returns an enumerator that iterates through the <see cref="Deque{T}" />.
        /// </summary>
        /// <returns>
        ///     An <see cref="Enumerator" /> for the <see cref="Deque{T}" />.
        /// </returns>
        public Enumerator GetEnumerator() { return new Enumerator(this); }

        /// <summary>
        ///     Copies the <see cref="Deque{T}" /> elements to a new array.
        /// </summary>
        /// <returns>
        ///     A new array containing elements copied from the <see cref="Deque{T}" />.
        /// </returns>
        public T[] ToArray()
        {
            var array = new T[Count];

            if (Count == 0) return array;

            CopyTo(array, 0);
            return array;
        }

        /// <summary>
        ///     Sets the capacity to the actual number of elements in the <see cref="Deque{T}" />, if that number is less than a
        ///     threshold value.
        /// </summary>
        /// <remarks>
        ///     This method can be used to minimize a collection's memory overhead if no new elements will be added to the
        ///     collection. To reset a <see cref="Deque{T}" /> to its initial state, call <see cref="Clear" /> before calling
        ///     <see cref="TrimExcess" />. Trimming an empty <see cref="Deque{T}" /> sets the capacity to the default capacity.
        /// </remarks>
        public void TrimExcess() => Capacity = Count;

        #endregion

        #region Interface Methods

        /// <summary>
        ///     Adds an object to the left end of the <see cref="Deque{T}" />.
        /// </summary>
        /// <param name="item">
        ///     The object to be added to the <see cref="Deque{T}" />. The value can be <c>null</c> for reference types.
        /// </param>
        public void PushLeft(T item)
        {
            EnsureCapacity(Count + 1);

            // Decrement left index and increment count
            LeftIndex--;
            Count++;

            // Insert item
            Left = item;

            _version++;
        }

        /// <summary>
        ///     Adds an object to the right end of the <see cref="Deque{T}" />.
        /// </summary>
        /// <param name="item">
        ///     The object to be added to the <see cref="Deque{T}" />. The value can be <c>null</c> for reference types.
        /// </param>
        public void PushRight(T item)
        {
            EnsureCapacity(Count + 1);

            // Increase count
            Count++;

            // Insert item
            Right = item;

            _version++;
        }

        /// <summary>
        ///     Returns the object at the left end of the <see cref="Deque{T}" /> without removing it.
        /// </summary>
        /// <returns>
        ///     The object at the left end of the <see cref="Deque{T}" />
        /// </returns>
        /// <exception cref="InvalidOperationException">
        ///     The <see cref="Deque{T}" /> is empty.
        /// </exception>
        public T PeekLeft()
        {
            if (Count == 0) throw new InvalidOperationException("The deque is empty");

            return Left;
        }

        /// <summary>
        ///     Returns the object at the right end of the <see cref="Deque{T}" /> without removing it.
        /// </summary>
        /// <returns>
        ///     The object at the right end of the <see cref="Deque{T}" />
        /// </returns>
        /// <exception cref="InvalidOperationException">
        ///     The <see cref="Deque{T}" /> is empty.
        /// </exception>
        public T PeekRight()
        {
            if (Count == 0) throw new InvalidOperationException("The deque is empty");

            return Right;
        }

        /// <summary>
        ///     Removes and returns the object at the left end of the <see cref="Deque{T}" />.
        /// </summary>
        /// <returns>
        ///     The object that is removed from the left end of the <see cref="Deque{T}" />.
        /// </returns>
        /// <exception cref="InvalidOperationException">
        ///     The <see cref="Deque{T}" /> is empty.
        /// </exception>
        public T PopLeft()
        {
            if (Count == 0) throw new InvalidOperationException("The deque is empty");

            // Retrieve left-most item and clean buffer slot
            var left = Left;
            Left = default(T);

            // Increment left index and decrement count
            LeftIndex++;
            Count--;
            _version++;

            return left;
        }

        /// <summary>
        ///     Removes and returns the object at the right end of the <see cref="Deque{T}" />.
        /// </summary>
        /// <returns>
        ///     The object that is removed from the right end of the <see cref="Deque{T}" />.
        /// </returns>
        /// <exception cref="InvalidOperationException">
        ///     The <see cref="Deque{T}" /> is empty.
        /// </exception>
        public T PopRight()
        {
            if (Count == 0) throw new InvalidOperationException("The deque is empty");

            // Retrieve rightmost item and clean buffer slot
            var right = Right;
            Right = default(T);

            // Decrement count
            Count--;
            _version++;
            return right;
        }

