        /// <summary>
        ///     Copies the collection elements to an existing one-dimensional <see cref="Array" />, starting at the specified array
        ///     index.
        /// </summary>
        /// <param name="array">
        ///     The one-dimensional <see cref="Array" /> that is the destination of the elements copied from the collection. The
        ///     Array must have zero-based indexing.
        /// </param>
        /// <param name="arrayIndex">
        ///     The zero-based index in <paramref name="array" /> at which copying begins.
        /// </param>
        /// <exception cref="ArgumentNullException">
        ///     <paramref name="array" /> is <c>null</c>.
        /// </exception>
        /// <exception cref="ArgumentOutOfRangeException">
        ///     <paramref name="arrayIndex" /> is less than 0.
        /// </exception>
        /// <exception cref="ArgumentException">
        ///     <paramref name="array" /> is multidimensional; or,
        ///     <paramref name="array" /> does not have zero-based indexing; or,
        ///     The number of elements in the source <see cref="Deque{T}" /> is greater than the available space from
        ///     <paramref name="arrayIndex" /> to the end of the destination <paramref name="array" />; or,
        ///     The type of the source <see cref="Deque{T}" /> cannot be cast automatically to the type of the destination array.
        /// </exception>
        void ICollection.CopyTo(Array array, Int32 arrayIndex)
        {
            if (array == null) throw new ArgumentNullException(nameof(array));

            if (array.Rank != 1)
                throw new ArgumentException("Only single dimensional arrays are supported for the requested action.");

            if (array.GetLowerBound(0) != 0)
                throw new ArgumentException("The destination array has a non-zero lower bound.");

            if (arrayIndex < 0 || arrayIndex >= array.Length)
                throw new ArgumentOutOfRangeException(nameof(arrayIndex),
                                                      "The index is invalid in the destination array.");

            if (array.Length - arrayIndex < Count) throw new ArgumentException("Destination array is not large enough");

            if (Count == 0) return;

            try
            {
                // If the elements are stored sequentially (i.e. the right-most index doesn't "wrap around" the array's boundary),
                // copy the internal array as a whole.
                if (!IndexExceedsBoundary)
                {
                    Array.Copy(_buffer, LeftIndex, array, arrayIndex, Count);
                }

                // Otherwise, copy both halves to the destination array.
                else
                {
                    Array.Copy(_buffer, LeftIndex, array, arrayIndex, Capacity - LeftIndex);
                    Array.Copy(_buffer, 0, array, arrayIndex + Capacity - LeftIndex,
                               LeftIndex                                + (Count - Capacity));
                }
            }
            catch (ArrayTypeMismatchException)
            {
                throw new
                    ArgumentException("Target array type is not compatible with the type of items in the collection.");
            }
        }

